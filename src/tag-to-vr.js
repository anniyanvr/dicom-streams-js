function adjustTag(tag) {
    if ((tag & 0xFFE00000) === 0x50000000 || (tag & 0xFFE00000) === 0x60000000)
        return tag & 0xFFE0FFFF;
    if ((tag & 0xFF000000) === 0x7F000000 && (tag & 0xFFFF0000) !== 0x7FE00000)
        return tag & 0xFF00FFFF;
    return tag;
}

