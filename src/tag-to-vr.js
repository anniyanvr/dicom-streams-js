const VR = require("./vr");
const Tag = require("./tag");

function adjustTag(tag) {
    if ((tag & 0xFFE00000) === 0x50000000 || (tag & 0xFFE00000) === 0x60000000)
        return tag & 0xFFE0FFFF;
    if ((tag & 0xFF000000) === 0x7F000000 && (tag & 0xFFFF0000) !== 0x7FE00000)
        return tag & 0xFF00FFFF;
    return tag;
}

module.exports = {
    vrOf: function (tag) {
        if ((tag & 0x0000FFFF) === 0) return VR.UL; // group length
        if ((tag & 0x00010000) !== 0)  // private creator ID
            return (tag & 0x0000FF00) === 0 && (tag & 0x000000F0) !== 0 ? VR.LO : VR.UN;
        if ((tag & 0xFFFFFF00) === Tag.SourceImageIDs) return VR.CS;
        const key = adjustTag(tag).toString(16);
        const vr = dict[key];
        return vr ? vr : VR.UN;
    }
}

